<h2>Summer Student Job: Refugee Settlement Assistant</h2>
</div>
<p></p>
<p>05.25.13</p>
<div class="newsbody"><p>*Thanks to a grant from Service Canada, this new position is available for the summer.*</p>
<p><strong>Time Commitment:</strong> Four weekdays and one weekend day per week (with some flexibility).&nbsp;Seven hours per shift, averaging 35 hours per week for 12 weeks.&nbsp;&nbsp;Exact schedule will be determined in consultation with successful candidate.</p>
<p><strong>Remuneration:</strong> $11.25/hour. <br />
<strong>Contract Length:</strong> June 3 2013 to August 31, 2013</p>
<p><strong>Objective:</strong> Assist in the running of a shelter for newly arrived refugee claimants. </p>
<p><strong>Conditions:</strong></p>
<ul>
<li>applicant must be a student returning to full-time studies in September, 2013 and who is between 18-30 years old</li>
<li>well organized, reliable, displays integrity, good communicator, a team player</li>
<li>legally entitled to work in Canada</li>
<li>not working 30 hrs or more per week at another job</li>
<li>Students with disabilities, Aboriginal students, and students who are members of visible minority groups will have priority in the selection process.</li>
<li>self motivated</li>
<li>committed to uphold Matthew House&rsquo;s values and standards</li>
<li>knowledge of Canada&rsquo;s refugee system and/or experience with newcomers an asset</li>
</ul>
<p><strong>Key Responsibilities:</strong></p>
<ul>
<li>Assist residents with their refugee claim process and accompany to appointments as required</li>
<li>Assist in conducting orientation sessions</li>
<li>Manage the office/shelter over the weekend</li>
<li>Assist residents in finding volunteer placements, accommodation and employment</li>
<li>Provide residents with orientation to Toronto and to new neighbourhood once they move into permanent accommodation.</li>
<li>Assist with application for permits (work and study) and renewal of documents;</li>
<li>Collaborate with Home Coordinator to ensure that the house is organized and that meals are served</li>
<li>Coordinate occasional weekend outings or special activities</li>
<li>Smooth running of the house during weekend shifts</li>
<li>Provide administrative assistance as needed</li>
<li>Attend summer camp (August 17-19) and weekly staff meetings</li>
</ul>
<p><strong>Assets:</strong></p>
<ul>
<li>knowledge of Spanish, French, Dari, Tigrigna, Amharic or Korean an asset</li>
<li>The following fields of studies are assets; Social Work, Refugee Law, Refugee Studies, International Development Studies, Management, Human Right and Equity</li>
</ul>
<p><strong>Reports to:</strong> Operations Director, Matthew House<br />
<strong>Application Deadline:</strong> Monday, May 29, 2013&nbsp;<br />
Please send r&eacute;sum&eacute; and cover letter by e-mail to:<br />
Astarekech Anjajo, Operations Director, Matthew House&nbsp;<a href="http://astar@matthewhouse.ca">astar@matthewhouse.ca</a></p>
<p>*We appreciate all applications but regret that only those selected for an interview will be contacted directly.&nbsp;</p></div>
