<h2>Volunteer Networking Event a Success!</h2>
</div>
<p></p>
<p>02.21.14</p>
<div class="newsbody"><p>Thank you to everyone who braved the sleet, rain and snow to come to the Volunteer Networking Event. It was a great evening and we were so happy to see many of you there.&nbsp;<br />
<br />
A special thank you to our guest speaker, Maureen Silcoff, who gave an informative talk about the Canadian refugee system. We were able to have a lively discussion and learn about the pressing issues facing the refugee system.&nbsp;<br />
<br />
For those who were unable to join us, do not fret, we hope to host another event later in the future (and hopefully this time the weather will cooperate). If you would like to be kept up-to-date about events happening at Matthew House please visit the Event section on our website.</p>
