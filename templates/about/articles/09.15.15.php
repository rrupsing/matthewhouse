<div class="newsbody"><p><strong>OPEN THE DOORS TO REFUGEES RALLY!</strong></p>
<p>Matthew House will be joining with other refugee support agencies in a national day of action this coming Thursday and you are all invited.&nbsp;</p>
<p>There will be a rally and march in downtown Toronto - to show our support for welcoming refugees - in solidarity with Syrian and other refugees in desperate need throughout the world.</p>
