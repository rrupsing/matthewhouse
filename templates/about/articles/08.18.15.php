<p>After a hugely successful &quot;pilot&quot; concert&nbsp;last week with some very enthusiastic residents, Matthew House is officially launching its &quot;Music in the Garden&quot; series every Wednesday at 7:30pm in our new beautifully transformed back&nbsp;garden at our Dundas St. home.</p>
<p>We are thrilled to have<a href="http://jeremyfosterlive.com/"> Jeremy Foster</a>, an accomplished young musician, as the curator for this series. Along with a network of professional musicians, they are offering free concerts for the Matthew House community.</p>
<p>We&nbsp;are&nbsp;thrilled to announce that&nbsp;<a href="http://thebombadils.com/music/">The Bombadils</a>&nbsp;will be live in concert tomorrow night.&nbsp;</p>
<p>Stay tuned and visit our <a href="https://www.facebook.com/matthewhousetoronto">Facebook page</a>&nbsp;for ongoing updates about future performers. We hope you can join us and enjoy&nbsp;some awesome music with amazing people from all corners of the world. Cheers!</p></div>
