<h2>Matthew House Celebrates World Refugee Day!</h2>
</div>
<p></p>
<p>06.19.15</p>
<div class="newsbody"><p>Thank you to our incredible volunteers, supporters and former residents who joined us tonight for a Celebration of World Refugee Day at our Dundas St. home. Highlights included an awesomely delicious dinner prepared by former residents from Afghanistan, Ethiopia and D.R. Congo, the signing of a &quot;Pledge&quot; to stand for a Canada that is strong on rights for refugees and enjoying traditionally prepared Ethiopian coffee in our newly created Healing Garden. All in all a wonderful evening.</p>
