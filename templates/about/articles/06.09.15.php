<h2>Emmanuel Jal lends his support to our screening of The Good Lie!</h2>
</div>
<p></p>
<p>06.09.15</p>
<div class="newsbody"><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p>Excitement is mounting about the screening of the film <strong>The Good Lie </strong>starring Emmanuel Jal and Rees Witherspoon at the Bloor Hot Docs Cinema tomorrow night (Wednesday, June 10th).&nbsp; The film depicts the incredible story of the journey of the &ldquo;lost boys&rdquo; of Sudan from war and danger to a place of safety.&nbsp;</p>
<p>Tickets are selling fast and we are honoured that&nbsp;even Emmanuel Jal has posted info about our special screening on his personal&nbsp;media sites&nbsp;encouraging Torontonians to come out in support of our work assisting newly arrived refugee claimants!&nbsp;</p>
<p>Not only will we be screening the film but the audience will be given a brief overview about of the plight of newly arrived refugees in Toronto&nbsp; and how Matthew House fills a great need.</p>
<p>Tickets are available online ( click related links on this website) or at the door, or call 416-364-8287.</p>
<p>See you at the film!</p>
