<h2>Have you heard of the ready tours?</h2>
</div>
<p></p>
<p>09.09.14</p>
<div class="newsbody"><p>We are so excited aboutthe READY Tours. &nbsp;It is a program that the Coalition of Service Providers for Refugee Claimants in Southern Ontario in partnership with the refugee board and the United Nations Refugee Agency have put together to help prepare refugee claimants for their hearing.</p>
<p>This bi-weekly tour allows refugee claiments the opportunity to help refugee claiments &nbsp;learn about what to do and expect at their proceeding.&nbsp;</p>
<p>Check out the article in the Toronto Star, which quotes our very own Samia Tecle (Refugee Claim Counselor), to learn more <strong><a href="http://www.thestar.com/news/immigration/2014/09/06/tours_help_refugee_claimants_navigate_asylum_hearings.html">click here</a></strong>.</p>
